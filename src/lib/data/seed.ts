const celebrityMap = new Map(celebrities.map((c) => [c.id, c]));
const postMap = new Map(posts.map((p) => [p.id, p]));

function buildRankings(): DailyRanking[] {
  const rankingDate = today.toISOString().split("T")[0];
  const scores = trendClusters.map((cluster) => ({
    trend_cluster_id: cluster.id,
    score: computeTrendScore(
      spottings.filter((s) => s.trend_cluster_id === cluster.id),
      celebrityMap,
      today
    ),
  }));

  const ranked = rankTrends(scores);

  return ranked.map((r) => ({
    id: `rank-${r.rank}`,
    date: rankingDate,
    trend_cluster_id: r.trend_cluster_id,
    score: r.score,
    rank: r.rank,
  }));
}

export const dailyRankings = buildRankings();

export function getTrendWithDetails(trendId: string): TrendWithDetails | null {
  const cluster = trendClusters.find((t) => t.id === trendId);
  if (!cluster) return null;

  const ranking = dailyRankings.find((r) => r.trend_cluster_id === trendId);
  const trendSpottings = spottings
    .filter((s) => s.trend_cluster_id === trendId)
    .map((s) => ({
      ...s,
      celebrity: celebrityMap.get(s.celebrity_id)!,
      post: postMap.get(s.post_id)!,
    }));

  const coverImage =
    trendSpottings[0]?.post.image_url ?? posts[0].image_url;

  return {
    ...cluster,
    score: ranking?.score ?? 0,
    rank: ranking?.rank ?? 0,
    cover_image_url: coverImage,
    spottings: trendSpottings,
  };
}

export function getRankedTrends(): TrendWithDetails[] {
  return dailyRankings
    .map((ranking) => getTrendWithDetails(ranking.trend_cluster_id))
    .filter((t): t is TrendWithDetails => t !== null);
}

export function getFilterOptions(): FilterOptions {
  const sigs = trendClusters.map((t) => t.attribute_signature);
  return {
    fits: [...new Set(sigs.map((s) => s.fit))],
    colours: [...new Set(sigs.map((s) => s.colour))],
    fabrics: [...new Set(sigs.map((s) => s.fabric))],
    patterns: [...new Set(sigs.map((s) => s.pattern))],
  };
}
